async function migrateArticles() {
  const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

  if (!supabaseUrl || !supabaseServiceKey) {
    console.error('âŒ Missing Supabase credentials. Please check your .env.local file.');
    console.error('   Required: NEXT_PUBLIC_SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY');
    process.exit(1);
  }

  const supabase = createClient(supabaseUrl, supabaseServiceKey);

  console.log('ðŸš€ Starting article migration...\n');

  let successCount = 0;
  let skipCount = 0;
  let errorCount = 0;

  for (const article of ARTICLES) {
    try {
      // Check if article already exists
      const { data: existing } = await supabase
        .from('articles')
        .select('id')
        .eq('slug', article.slug)
        .maybeSingle();

      if (existing) {
        console.log(`â­ï¸  Skipping "${article.title}" - already exists`);
        skipCount++;
        continue;
      }

      // Find or create admin user to use as author_id
      // If no admin found, we'll use null and rely on author_name
      const { data: adminUser } = await supabase
        .from('user_roles')
        .select('user_id')
        .eq('role', 'admin')
        .limit(1)
        .maybeSingle();

      // Insert article
      const { data, error } = await supabase
        .from('articles')
        .insert({
          slug: article.slug,
          title: article.title,
          excerpt: article.excerpt,
          content: article.content,
          category: article.category,
          tags: article.tags || [],
          author_name: article.author,
          author_id: adminUser?.user_id || null,
          date: article.date,
          is_new: article.isNew || false,
          is_published: true, // Publish all migrated articles
          published_at: new Date().toISOString(),
          // Set SEO fields
          meta_title: article.title,
          meta_description: article.excerpt,
        })
        .select()
        .single();

      if (error) {
        console.error(`âŒ Error migrating "${article.title}":`, error.message);
        errorCount++;
      } else {
        console.log(`âœ… Migrated "${article.title}" (ID: ${data.id})`);
        successCount++;
      }
    } catch (error) {
      console.error(`âŒ Unexpected error for "${article.title}":`, error.message);
      errorCount++;
    }
  }

  console.log('\nðŸ“Š Migration Summary:');
  console.log(`   âœ… Success: ${successCount}`);
  console.log(`   â­ï¸  Skipped: ${skipCount}`);
  console.log(`   âŒ Errors: ${errorCount}`);
  console.log(`   ðŸ“ Total: ${ARTICLES.length}`);

  if (errorCount === 0) {
    console.log('\nðŸŽ‰ Migration completed successfully!');
  } else {
    console.log('\nâš ï¸  Migration completed with errors. Please review the output above.');
    process.exit(1);
  }
}

// Run migration
migrateArticles().catch((error) => {
  console.error('ðŸ’¥ Fatal error:', error);
  process.exit(1);
});
